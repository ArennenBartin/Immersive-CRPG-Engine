// ── Hand-authored actor pixel art ─────────────────────────────────────────────
// Base bodies drawn by hand per style × facing × frame; entities palette-swap
// the body/accent ramps (B/D/H and A/a/h) while skin, eyes and fixed details
// stay constant. West frames are mirrored from east. Styles whose lore fixes
// their gaze (echoes, the screaming-faced things) reuse the south face — the
// face is always toward you.

import {
  C,
  rampDark,
  rampLight,
  type OverworldPaletteId,
} from "./overworldPalette";
import {
  FIXED_CHARS,
  art,
  grid,
  mirrorRows,
  paint,
  type CharMap,
  type Pixels,
} from "./overworldPixelArt";

export type ArtDirection = "north" | "south" | "east" | "west";
export type ArtFrame = "idle" | "step";

type FrameArt = { idle: string; step: string };
type StyleArt = { south: FrameArt; north?: FrameArt; east?: FrameArt };

const entityMap = (
  body: OverworldPaletteId,
  accent: OverworldPaletteId,
): CharMap => ({
  ...FIXED_CHARS,
  B: C(body),
  D: rampDark(body),
  H: rampLight(body),
  A: C(accent),
  a: rampDark(accent),
  h: rampLight(accent),
  S: C("skin"),
  s: rampDark("skin"),
  P: C("ink"),
  E: C("white"),
});

