const frameFor = (style: StyleArt, facing: ArtDirection): { rows: FrameArt; mirrored: boolean } => {
  if (facing === "north") return { rows: style.north ?? style.south, mirrored: false };
  if (facing === "east") return { rows: style.east ?? style.south, mirrored: false };
  if (facing === "west") return { rows: style.east ?? style.south, mirrored: !!style.east };
  return { rows: style.south, mirrored: false };
};

export const entityStylePixels = (
  styleId: string,
  body: OverworldPaletteId,
  accent: OverworldPaletteId,
  facing: ArtDirection,
  frame: ArtFrame,
): Pixels => {
  const style = STYLE_ART[styleId] ?? HUMANOID;
  const { rows, mirrored } = frameFor(style, facing);
  const name = `ent_${styleId}_${facing}_${frame}`;
  const parsed = grid(name, rows[frame]);
  return paint(name, mirrored ? mirrorRows(parsed) : parsed, entityMap(body, accent));
};

